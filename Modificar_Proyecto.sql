CREATE PROCEDURE MODIFICAR_PAGOS
@MONUMERO_PAGOS INT,
@MOVALOR_PAGOS FLOAT,
@MOCODIGO_FORMAPG_PAGOS_FK VARCHAR(10),
@MOFECHA_PAGOS datetime,
@MOCODIGO_EMPLEADO_PAGOS_FK NUMERIC,
@MOCODIGO_CLIENTE_PAGOS_FK NUMERIC,
@MOCODIGO_FACTVENT_PAGOS_FK NUMERIC,
@MOCODIGO_FACTCOMP_PAGOS_FK NUMERIC,
@MOESTADO_PAGOS VARCHAR(10)
AS
UPDATE PAGOS
SET Valor_Pagos = @MOVALOR_PAGOS,Codigo_FormaPG_Pagos_FK = @MOCODIGO_FORMAPG_PAGOS_FK,Fecha_Pagos = @MOFECHA_PAGOS,Codigo_Empleado_Pagos_FK = @MOCODIGO_EMPLEADO_PAGOS_FK,Codigo_Cliente_Pagos_FK = @MOCODIGO_CLIENTE_PAGOS_FK,Codigo_FactVent_Pagos_FK = @MOCODIGO_FACTVENT_PAGOS_FK,Codigo_FactComp_Pagos_FK = @MOCODIGO_FACTCOMP_PAGOS_FK, Estado_Pagos = @MOESTADO_PAGOS
WHERE @MONUMERO_PAGOS = Numero_Pagos