use Proyecto

CREATE PROCEDURE MODIFICAR_INI_SESION
@MOUSUARIOSESION varchar(50),
@MOCONTRASEÑASESION varchar(20),
@MONOMBRESESION varchar(35),
@MOROLSESION varchar(35),
@MOESTADOSESION varchar(10)
AS
UPDATE INICIO_SESION
SET Contraseña_Usuario = @MOCONTRASEÑASESION, Nombre_Usuario = @MONOMBRESESION,Rol_Usuario = @MOROLSESION, Estado_Usuario = @MOESTADOSESION
WHERE @MOUSUARIOSESION = Usuario_Usuario

CREATE PROCEDURE MODIFICAR_CARGO
@MOCODIGOCARGO varchar(10),
@MONOMBRECARGO varchar(15),
@MOESTADOCARGO varchar(10)
AS
UPDATE CARGO
SET Nombre_Cargo = @MONOMBRECARGO,Estado_Cargo = @MOESTADOCARGO
WHERE @MOCODIGOCARGO = Codigo_Cargo

